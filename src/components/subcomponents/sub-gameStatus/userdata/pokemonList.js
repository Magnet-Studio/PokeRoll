const TenerDate = () => {

    let fecha = new Date();
    let dia = String(fecha.getDate()).padStart(2, '0');
    let mes = String(fecha.getMonth() + 1).padStart(2, '0');
    let año = fecha.getFullYear();
    
    let fechaActual = dia + '/' + mes + '/' + año;

    return (fechaActual);
}

